class Product {
    constructor(id, ownerId, Product_Name, Product_Group, Title, Description, Price, Image) {
        this.id = id;
        this.ownerId = ownerId;
        this.Product_Name = Product_Name;
        this.Product_Group = Product_Group;
        this.Title = Title;
        this.Description = Description;
        this.Price = Price;
        this.Image = Image;
    }
}

export default Product;